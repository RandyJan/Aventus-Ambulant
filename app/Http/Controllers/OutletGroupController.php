    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $items = Group::whereHas('partLocation', function($q) use($request){
            $q->where('BRANCHID', $request->branch_id)
            ->where('OUTLETID', $request->outlet_id);

        })
        ->when($request->search != 'All', function($q) use($request){
            $q->where('DESCRIPTION', 'LIKE', "%$request->search%");
        })
        ->get();

        return new GroupCollection($items);
    }

